<h1>Однофазное УПП TSG 2.2 230VAC для снижения пусковых токов при запуске двигателей, насосов, насосных станций</h1>
<p>Очень часто пользователи автономных генераторов электрической энергии сталкиваются с одной и той же проблемой, которую все описывают по-разному: «проседает» генератор, временно падает напряжение в сети, генератор глохнет во время пуска насосной станции и т.п. Если используемое оборудование исправно, то проблема, как правило, возникает при включении «мощных» механизмов, имеющих в своём составе асинхронный двигатель.</p>
<p>В бытовых насосных станциях чаще всего используются асинхронные двигатели. Соответственно в более производительные насосные станции устанавливаются более мощные электродвигатели.</p>
<blockquote>Во избежание возникающих при пуске насосной станции процессов пагубных для механической и электрической части нужно использовать устройство плавного пуска (УПП), так называемый, софтстартер.</blockquote>
