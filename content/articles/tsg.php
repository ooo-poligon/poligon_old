<p>Чем более мощный электродвигатель приводит в действие насос, тем больший пусковой  ток возникает при его запуске. Если поставить эксперимент и включать в сеть поочерёдно асинхронные  двигатели и постоянно увеличивать их мощность, то можно заметить, что в момент пуска более мощных электродвигателей их влияние на параметры питающей сети растёт. Все более заметным становится падение напряжения в питающей сети, так лампы накаливания на мгновение светят менее ярко, чем обычно, лампы дневного света могут гаснуть и снова зажигаться, в чувствительной электронике может срабатывать защита от перепадов напряжения. Так на напряжение питающей сети оказывает действие пусковой ток асинхронного двигателя, который может превышать номинальный ток в 2-8 раз. Такие импульсные броски тока могут стать причиной выхода из строя другого оборудования, чувствительного к качеству питающего напряжения, и подключенного к той же фазе, что и двигатель. </p>
<p>В промышленности, где проблема пусковых токов стоит остро из-за использования мощных асинхронных двигателей давно используют различные методы, позволяющие уменьшить пусковые токи и нагрузку на всю энергосистему предприятия в целом.
В бытовых условиях, обычно, доступно лишь однофазное напряжение, поэтому и асинхронные двигатели используются однофазные, их ещё называют конденсаторными.</p>
<p>Во избежание возникающих при пуске насосной станции процессов пагубных для механической и электрической части нужно использовать устройство плавного пуска (УПП), так называемый, софтстартер. </p>
<p>Устройства плавного пуска обеспечивают медленное увеличение напряжения, подаваемого на электродвигатель (насос) при его запуске, тем самым достигается плавный разгон электродвигателя и существенное снижение значений пускового тока и крутящего момента. На рисунке 1 представлены графики крутящего момента во времени, в зависимости от способа подключения асинхронного двигателя с короткозамкнутым ротором к сети питания (с использование УПП и без). </p>


