<h3>Часто задаваемые вопросы:</h3>

<dl>
<dt>В: Чем отличаются устройства плавного пуска TSG 2,2 230VAC и TSG 2,2 400VAC?</dt>
<dd>О: Это два разных устройства, TSG 2,2 230VAC предназначено для запуска 1-фазных электродвигателей 220-230VAC мощностью до 1,3кВт, TSG 2,2 400VAC предназначено для пуска 3-фазных электродвигателей 380-400VAC (либо 2-фазных электродвигателей 380-400VAC) мощностью до 2,2кВт. Причём  оба эти устройства осуществляют плавный пуск только по одной фазе, поэтому для 3-фазных электродвигателей мы рекомендуем использовать устройства плавного пуска серии MSG.</dd>

<dt>В: Какова схема включения УПП TSG 2,2 230VAC для однофазного двигателя</dt>
<dd>О: Устройства плавного пуска включается непосредственно в цепь питания электродвигателя. Сигнал управления, который ранее использовался для управления контактором, напрямую соединявшим цепь питания двигателя с сетью питания, теперь должен управлять активизацией УПП. Схема подключения приведена на рис. 6 и в инструкции на устройство.</dd>

<dt>В: Насколько часто можно производить запуск двигателя с УПП?</dt>
<dd>О: Стандартная величина циклов запуска для УПП серии TSG и MSG – 30 циклов в час (при средней нагрузке). Во время запуска происходит выделение тепла, поэтому требуется время на охлаждение устройства.</dd>

<dt>В: Для чего нужен регулятор Mon?</dt>
<dd>О: Регулятор Mon позволяет определить начальный момент вращения (начальное напряжение), с которого будет начат цикл плавного пуска, если Mon установлен в 0, то при осуществлении цикла запуска двигатель некоторое время будет оставаться неподвижным, опытным путём можно подобрать такое значение Mon, при котором двигатель сразу же будет начинать вращение и постепенно достигнет рабочего режима. Важно: увеличение начального пускового момента  Mon увеличивает пусковой ток!</dd>

<dt>В: Для чего нужен регулятор Ton?</dt>
<dd>О: Регулятор Ton используется для задания времени, в течение которого будет производиться плавный пуск двигателя от Mon до 100% момента вращения (напряжения), по его окончании будет активизирован шунтирующий контактор. Важно: уменьшение времени запуска Ton увеличивает пусковой ток! </dd>

<dt>В: Что такое шунтирующий контактор? </dt>
<dd>О: Шунтирующий контактор (байпас-контактор) замыкает цепь питания электродвигателя напрямую от сети питания, в обход устройства плавного пуска, сразу после того, как цикл плавного запуска электродвигателя завершён. Устройства плавного пуска серии TSG и MSG имеют шунтирующий контактор в своём составе, таким образом, его не требуется приобретать дополнительно.</dd>

<dt>В: Можно ли запитать с выходов УПП другие бытовые приборы, кроме двигателя.</dt>
<dd>О: Устройство плавного пуска предназначено для плавного запуска электродвигателя, подключение других потребителей может привести к выходу из строя как потребителей, так и самого устройства. </dd>

<dt>В: Какие дополнительные аксессуары понадобятся для работы УПП серий TSG и MSG?</dt>
<dd>О: Дополнительные аксессуары для работы устройств плавного пуска данных серий не требуются.</dd>
</dl>

