<p>Устройство плавного пуска TSG активируется, когда переменное напряжение сети питания подаётся на выводы L1 и L3 (LED U ВКЛ). Напряжение на выходе Т1 увеличивается линейно в течение заданного времени до достижения максимального значения; на выходы T2 и T3  постоянно подаётся напряжение сети питания. Время, в течение которого происходит этот процесс можно плавно регулировать с помощью регулятора TON в диапазоне от 0 до 20 секунд. С увеличением напряжения увеличивается и момент вращения. Таким образом, происходит запуск двигателя с постоянным разгоном. По окончании цикла запуска, двигатель напрямую подключается к сети питания с помощью встроенного шунтирующего (байпас) контактора.</p>
