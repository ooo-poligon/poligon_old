<p>Из таблицы видно, что всего за неделю продолжительность светового дня уменьшилась на 32 минуты. При использовании обычного (не астрономического) таймера, приходилось бы периодически перенастраивать время включения и отключения освещения. Астрономический же таймер требуется настроить один раз, в дальнейшем он не требует вмешательства пользователя. </p> 

<h2>Примеры алгоритмов, которые можно организовать с помощью астрономических таймеров.</h2>

<p>1.	<entity name="talento 791 pro" prop="link"/> – астрономический таймер. Освещение включено постоянно в темное время суток. Для освещения городских улиц, заправочных станций, рекламных щитов, фасадов архитектурных сооружений, автостоянок и т.п. Астрономический таймер предоставляет возможность установить время включения/выключения не точно в расчетный момент заката/восхода, а с опережением или запаздыванием относительно расчетного до 90 минут с шагом в 1 минуту
</p>

<img src="/images/articles/all-night.jpg" alt="график работы всю ночь"/>


<p>2.	<entity name="talento 792 pro" prop="link"/> – астрономический таймер + недельная программа. Освещение включено в темное время суток только в те моменты, когда оно необходимо. Освещение придомовой территории, крыльца частного дома, как правило, требуется лишь сразу после заката, до тех пор, пока люди не уснули и некоторое время до рассвета, когда люди уже проснулись, а солнце еще не поднялось над горизонтом. В остальное время, днем и глубокой ночью, освещение не требуется. </p>

<img src="/images/articles/not-all-night.jpg" alt="график работы не всю ночь"/>

<p>Кроме того, при необходимости, освещение всегда можно включить нажатием всего лишь одной кнопки на астрономическом таймере.</p>
