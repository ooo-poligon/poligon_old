<h1>Управление во времени. Астрономические таймеры Graesslin.</h1>
<img src="/images/articles/graeslin-head-logo-logo.jpg" alt=""/>
<p>Для автоматизации освещения улиц, территорий автостоянок, фасадов зданий и прилегающих территорий существует простое и надежное решение – использования астрономических таймеров компании Graesslin (Германия).</p>

<img src="/images/articles/123.jpg" alt="" style="float: left; padding: 16px; width: 224px;" />

<p>Такое решение поможет рационально использовать освещение и сократить затраты на электроэнергию.</p>

<p>Как правило, освещение требуется только в темное время суток. Продолжительность светового дня в течение года изменяется, соответственно постоянно изменяются время восхода и захода солнца, кроме того, на степень освещенности могут влиять окружающие объекты и погодные условия.</p> 

<p>Рассмотрим два возможных варианта решения управления освещением с использованием астрономического таймера <entity name="talento 791 pro" prop="link"/> или <entity name="talento 792 pro" prop="link"/>. </p>

<img src="/images/articles/talento-device.jpg" alt="" style="float: right; width: 144px;" class="show"/>
<p>Астрономический таймер отличается от обычных таймеров тем, что сам автоматически вычисляет время восхода и захода солнца и в соответствии с вычисленными значениями включает и выключает освещение. Поэтому для того чтобы астрономический таймер функционировал корректно, при его программировании кроме текущей даты и времени, необходимо ввести географические координаты вашего местоположения – широту и долготу (выбрать ваше местоположение можно из списка городов или посмотреть карту).
</p>


