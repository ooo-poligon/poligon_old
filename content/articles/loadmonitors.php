<h4>Сигнал на выходе:</h4>
<p><strong>Релейный контакт</strong> – наиболее типичный вариант. Позволяет непосредственно осуществлять управляющее воздействие (запрет на работу установки) или информировать об обнаруженном сборе (лампочка на пульте управления). Некоторые устройства имеют 2 перекидных контакта и позволяют поставить разные пороги срабатывания для каждого. </p>
<p><strong>Стандартный сигнал 4..20мА</strong> – позволяют завести данные в контроллер и далее обрабатывать их с помощью специальной программы (накопление данных, сопоставление между разными периодами для выявления незначительных нарастающих отклонений в работе, сбор статистики и т.д.)</p>
<p><strong>Передача данных в промышленные сети</strong> – позволяет добавить нужную функциональность в уже существующую конфигурацию оборудования. </p>

<h4>Преимущества реле контроля мощности и коэффициента мощности (cos &phi;):</h4>
<ul style="list-style-type: none;">
	<li><strong>- предельная простота при монтаже</strong> (в существующий щит, не требуется механических работ, как, например, при монтаже тензодатчиков)</li>
	<li><strong>- позволяют добавить функциональность в уже смонтированные системы</strong> при минимальных затратах времени, сил и средств</li>
	<li><strong>- уверенное определение ситуаций сбоя</strong> (перегруз, обрыв ремня, сухой ход, закрытый вентиль и проч.)</li>
	<li><strong>- простота настройки</strong> (достаточно определить номинальное значение на работающей системе и установить пороги срабатывания)</li>
</ul>

