<p><strong>Реле контроля нагрузки</strong> позволяют без <u>применения дополнительных компонентов</u> контролировать различные варианты сбоя в работе промышленного оборудования, имеющего своим основным элементов двигатель или насос. Для этого реле контроля подключается в цепь питания электродвигателя (насоса), измеряет активную мощность или коэффициент мощности (cos &phi;) и осуществляет управляющее воздействие при выходе контролируемых значений за предустановленные пороги срабатывания. </p>

<h3>Примеры применения для двигателей:</h3>
<ul class="mark">
	<li>ограничение грузоподъемности кранового оборудования</li>
	<li>контроль свободного места в пресс-компакторах</li>
	<li>отсутствие потока воздуха, засоренные фильтры, в вентиляционных системах</li>
	<li>износ или повреждение режущего/сверлящего инструмента в станках</li>
	<li>наличие/отсутствие обрабатываемого материала в станках</li>
	<li>повреждение сверла или режущего элемента в станках </li>
	<li>определение вязкости перемешиваемой субстанции (миксеры)</li>
	<li>контроль работы конвейеров (обрыв клинового ремня, блокировка, наличие и количество груза) </li>
	<li>блокировка, недогрузка и простой, износ элементов дробильного оборудования</li>
</ul>

<h3>Примеры применения для насосов:</h3>
<ul class="mark">
	<li>защита насосов от сухого хода (без датчиков!)</li>
	<li>перегруз при застревании инородного материала или засорении канала</li>
	<li>определение наличия закрытых вентилей </li>
	<li>засоренные фильтры</li>
	<li>износ механических деталей</li>
	<li>разрушение рабочего колеса вследствие воздействия кавитации</li>
</ul>
