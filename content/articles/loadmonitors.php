<h4>Какие варианты контроля нагрузки существуют?</h4>
<p><strong>Реле контроля активной мощности (P)</strong> – позволяют уверенно контролировать как ситуацию перегрузки, так и ситуацию недогрузки. Наиболее универсальный вариант контроля.</p>
<p><strong>Реле контроля коэффициента мощности (cos &phi;)</strong> – позволяют уверенно определять только ситуацию недогрузки, т.к. коэффициент мощности сильно изменяется в этом случае, но практически не изменяется при возникновении ситуации перегрузки.</p>
<p><strong>Реле контроля тока (I)</strong> – позволяют уверенно определять только ситуацию перегрузки, т.к. в этом случае ток сильно возрастает.</p>

