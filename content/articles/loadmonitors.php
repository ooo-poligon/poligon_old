<p>В виду того, что реле контроля мощности и коэффициента мощности cos &phi; контролируют <strong>не параметры цепи измерения</strong> (как, например, <a href="/content/reviews/tele-phase-monitor-review.php">реле контроля фаз</a>), а именно <strong>состояние установки</strong> в работе, их так же называют реле контроля нагрузки. Под нагрузкой тут подразумевается именно контролируемое оборудование. </p>

<p><strong>Для вычисления активной мощности или коэффициента мощности (cos &phi;)</strong> необходимо измерить величины напряжений и ток по одной из фаз, непосредственно в цепи питания электродвигателя (насоса).</p>

<p><strong>Реле контроля нагрузки</strong> могут применяться для 1- и 3-фазных нагрузок. </p>

<p><strong>Наблюдение за изменением мощности, потребляемой электродвигателем (или насосом)</strong> во время работы позволяют делать ряд полезных выводов о состоянии как непосредственно самого двигателя (насоса), так и, что более важно, о состоянии всей установки (станка, конвейера, дробилки, вентиляционной системы, пресс-компактора и т.п.).<br/>
Это позволяет с успехом использовать их для защиты оборудования от критических сбоев или для заблаговременного информирования о необходимости технического обслуживания.</p>

