<h2>Защита насоса от «сухого хода»: электродвигатель как датчик</h2>
 
<p>Для защиты насоса от «сухого хода», то есть для предотвращения работы насоса без жидкости используют различные устройства: поплавковые выключатели, реле остаточного давления, реле протока. Скважные насосы также защищают с помощью специальных электродов и управляющего контроллера.</p>
 <img src="/images/nasos_cit.jpg" style="width: 540px;" /> 
<br />
 
<p>Обо всех этих способах защиты насоса от «сухого хода» написано огромное количество материалов, рекламных статей, сняты видеоролики. Но все эти способы подразумевают определение наличия перекачиваемой жидкости, непосредственно контактируя с ней.</p>
 
<p>В том или ином виде подразумевается установка дополнительного датчика. А не может ли сам насос давать информацию о режиме «сухого хода»? Ответ положительный! Электродвигатель, приводящий насос в движение сам по себе является датчиком «сухого хода». </p>
 
<p>Рассмотрим два режима работы насоса:</p>
 
<ol> 
  <li><strong>Работа при наличии воды.</strong> Насос перекачивает жидкость, при этом электродвигатель работает в номинальном режиме, с номинальной нагрузкой. При этом коэффициент мощности (cos &#966;) стремится к номинальному значению для этого конкретного электродвигателя, примерно 0.7-0.8. 
    <p></p>
   </li>
 
  <li><strong>Работа насоса в режиме «сухого хода».</strong> Насос не перекачивает жидкость, при этом электродвигатель перестает выполнять полезную работу, работает без нагрузки. При этом коэффициент мощности (cos &#966;) резко уменьшается до значения 0.25-0.4 
    <p></p>
   </li>
 </ol>
 
<p>Уменьшение коэффициента мощности электродвигателя является показателем того, что насос работает в режиме «сухого хода»!</p>
 
<p>На графике видно как изменяется значение коэффициента мощности при работе насоса в нормальном режиме и в режиме «сухого хода»:</p>
 <img src="/images/nasos_graph.jpg" alt="Зависимость коэффициента мощности от нагрузки на валу" /> 
<br />
 
